static void store_vid_tag(struct multiboot_header_tag *mboot_tag)
{
	fb_tag = (struct multiboot_fb_tag *)find_multiboot_table(mboot_tag, 8);
	if(!fb_tag)
		kerror(ERR_MEDERR, "Mutiboot framebuffer tag not found, trying to use default video mode");
}

static inline u32 bit_mask(u32 x)
{
	return (x >= sizeof(u32) * 8) ? (u32) -1 : (1U << x) - 1;
}

void init_video(struct multiboot_header_tag* mboot_tag)
{
	store_vid_tag(mboot_tag);
	
	vid = (void *)(ptr_t)fb_tag->fb_addr;
	width  = fb_tag->fb_width;
	height = fb_tag->fb_height;
	pitch  = fb_tag->fb_pitch;
	bpp    = fb_tag->fb_bpp;
	type   = fb_tag->type;
	stride = fb_tag->fb_pitch;
	
	if(type == 1)
	{
		fb_rgb = (struct multiboot_fb_type_rgb *)(fb_tag + (sizeof(fb_tag)));
		
		red_mask   = bit_mask(fb_rgb->fb_red_mask_size)   << fb_rgb->fb_red_field_pos;
		green_mask = bit_mask(fb_rgb->fb_green_mask_size) << fb_rgb->fb_green_field_pos;
		blue_mask  = bit_mask(fb_rgb->fb_blue_mask_size)  << fb_rgb->fb_blue_field_pos;
		
		red_pos   = fb_rgb->fb_red_field_pos;
		green_pos = fb_rgb->fb_green_field_pos;
		blue_pos  = fb_rgb->fb_blue_field_pos;
	}
	
	
	kerror(ERR_INFO, "Video type reported by GRUB: %d", (u32)fb_tag->fb_type);
	kerror(ERR_INFO, "  -> Resolution: %dx%d",          (u32)fb_tag->fb_width, (u32)fb_tag->fb_height);
	kerror(ERR_INFO, "  -> Address:  0x%08X",           (u32)fb_tag->fb_addr);
	kerror(ERR_INFO, "  -> Pitch:    0x%X",             (u32)fb_tag->fb_pitch);
	kerror(ERR_INFO, "  -> BPP:        %d",             (u32)fb_tag->fb_bpp);
}

void mboot_putpix(int x, int y, u8 r, u8 g, u8 b)
{
	//*(u16 *)(vid + ((x * 2) + (y * stride))) = (((r << red_pos) & red_mask) | ((g << green_pos) & green_mask) | ((b << blue_pos) & blue_mask));
	if(bpp == 24)
	{
		*(u8 *)(vid + ((x * 3) + (y * stride)) + 0) = r;
		*(u8 *)(vid + ((x * 3) + (y * stride)) + 1) = g;
		*(u8 *)(vid + ((x * 3) + (y * stride)) + 2) = b;
	}
}

static inline void put_char_line(int x, int y, int l, u8 c, u8 r, u8 g, u8 b)
{
	if(font[c*16+l] & 0x80) mboot_putpix(x+0, y, r, g, b);
	if(font[c*16+l] & 0x40) mboot_putpix(x+1, y, r, g, b);
	if(font[c*16+l] & 0x20) mboot_putpix(x+2, y, r, g, b);
	if(font[c*16+l] & 0x10) mboot_putpix(x+3, y, r, g, b);
	if(font[c*16+l] & 0x08) mboot_putpix(x+4, y, r, g, b);
	if(font[c*16+l] & 0x04) mboot_putpix(x+5, y, r, g, b);
	if(font[c*16+l] & 0x02) mboot_putpix(x+6, y, r, g, b);
	if(font[c*16+l] & 0x01) mboot_putpix(x+7, y, r, g, b);
}

void mboot_putxy(int x, int y, u8 c, u8 r, u8 g, u8 b)
{
	put_char_line(x, y+0,  0,  c, r, g, b);
	put_char_line(x, y+1,  1,  c, r, g, b);
	put_char_line(x, y+2,  2,  c, r, g, b);
	put_char_line(x, y+3,  3,  c, r, g, b);
	put_char_line(x, y+4,  4,  c, r, g, b);
	put_char_line(x, y+5,  5,  c, r, g, b);
	put_char_line(x, y+6,  6,  c, r, g, b);
	put_char_line(x, y+7,  7,  c, r, g, b);
	put_char_line(x, y+8,  8,  c, r, g, b);
	put_char_line(x, y+9,  9,  c, r, g, b);
	put_char_line(x, y+10, 10, c, r, g, b);
	put_char_line(x, y+11, 11, c, r, g, b);
	put_char_line(x, y+12, 12, c, r, g, b);
	put_char_line(x, y+13, 13, c, r, g, b);
	put_char_line(x, y+14, 14, c, r, g, b);
	put_char_line(x, y+15, 15, c, r, g, b);
	put_char_line(x, y+16, 16, c, r, g, b);
}

void mbootvid_clear()
{
	u32 i = 0;
	for(; i < width*height*3; i++)
		*(u8 *)(vid + i) = 0x00;
}

static void scrollup()
{
	u32 i = 0;
	for(; i < (width * (height - 16) * 3); i++)
		*(u8 *)(vid + i) = *(u8 *)(vid + i + width * 3);
	
	for(; i < (width * (height) * 3); i++)
		*(u8 *)(vid + i) = 0x00;
}

void mboot_put(char c)
{
	switch(c)
	{
		case 0x00:
			return;

		case '\t':
			xpos = (xpos + 8*8) & ~(8*8);
			break;

		case '\n':
			xpos = 0;
			ypos+=16;
			break;

		default:
			mboot_putxy(xpos, ypos, (u8)c, mboot_red, mboot_green, mboot_blue);
			xpos+=8;
			break;
	}
	if(xpos >= width) { xpos = 0; ypos+=16; }
	if(ypos >= height) { ypos = height - 16; scrollup(); }
}





